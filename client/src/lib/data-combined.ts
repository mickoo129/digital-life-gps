export const combinedNumberLookup: Record<string, CombinedNumberData> = {};
Object.values(combinedNumbersData).forEach(data => {
  data.aliases.forEach(alias => {
    combinedNumberLookup[alias] = data;
  });
});

// 查找聯合數字
export function lookupCombinedNumber(input: string): CombinedNumberData | null {
  const trimmed = input.trim();
  
  // Direct match
  if (combinedNumberLookup[trimmed]) {
    return combinedNumberLookup[trimmed];
  }
  
  // Try reverse order (e.g., 213 -> 123/213)
  const reversed = trimmed.split("").reverse().join("");
  if (combinedNumberLookup[reversed]) {
    return combinedNumberLookup[reversed];
  }
  
  // Try sorted order
  const sorted = trimmed.split("").sort().join("");
  for (const [alias, data] of Object.entries(combinedNumberLookup)) {
    const aliasSorted = alias.split("").sort().join("");
    if (aliasSorted === sorted) {
      return data;
    }
  }
  
  return null;
}
